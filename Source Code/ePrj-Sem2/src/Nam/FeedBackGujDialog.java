    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        if (txtAreaFeedback.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please give us your feedback.");
            return ;
        }
        if (txtIDApa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "This field cannot be blank..");
            return ;
        }
        if (txtDate.getDate() == null) {
            JOptionPane.showMessageDialog(this, "This field cannot be blank..");
            return ;
        }
        
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String dof = df.format(txtDate.getDate());
        
        sql = "insert into FeedbackNote(DateFb, IDGu, IDApa, ContentFb, StatusFb) values ('"+dof+"', '"+txtIDGU.getText()+"', '"+txtIDApa.getText()+"', '"+txtAreaFeedback.getText()+"', 'unread')";
        try {
            stmt.executeUpdate(sql);
        } catch (SQLException ex) {
            Logger.getLogger(FeedBackGujDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JOptionPane.showMessageDialog(this, "Thanks for your feedback!!!");
        dispose();
    }//GEN-LAST:event_btnSendActionPerformed

