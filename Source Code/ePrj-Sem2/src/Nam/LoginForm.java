    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        //Ket noi database
        try {
            connection = DBConnection.getDBConnection(dbName, dbAccount, dbPassword, port);
            stmt = connection.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
        } catch (Exception e) {
            e.printStackTrace();
        }

        //Kiem tra trang
        if (txtUsername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username cannot be blank - Please re-enter.");
            txtUsername.grabFocus();
            return;
        }
        if (txtPassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password cannot be blank - Please re-enter.");
            txtPassword.grabFocus();
            return;
        }

        //thuc hien kiem tra login
        loadAccountList();
        boolean check = false;
        for (String user : listID) {
            if (user.equals(txtUsername.getText())) {
                sql = "select * from Account where ID = " + "'" + user + "'";
                try {
                    rs = stmt.executeQuery(sql);
                    rs.beforeFirst();
                    while (rs.next()) {
                        password = rs.getString("Password");
                        active = rs.getBoolean("Active");
                    }
                    //check = true;
                    break;
                } catch (SQLException ex) {
                    Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        if (password.equals(txtPassword.getText())) {
            check = true;
        }
        if (check == true && active) {
            mcf = new MainControlInterface(txtUsername.getText(), txtDatabase.getText(), txtDBUser.getText(), txtDBPass.getText(), txtPort.getText());
            mcf.setVisible(true);
            dispose();
        }
        if (check == true && !active) {
            JOptionPane.showMessageDialog(this, "This account is blocked.");
            return;
        }
        if (check == false) {
            JOptionPane.showMessageDialog(this, "Username or Password is not correct.");
            textReset();
            txtUsername.grabFocus();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

