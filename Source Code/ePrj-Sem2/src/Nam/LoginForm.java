    public LoginForm(String account, String dbName, String dbAccount, String dbPassword, String port) {
        initComponents();
        setLocationRelativeTo(null);
        try {
            connection = DBConnection.getDBConnection(dbName, dbAccount, dbPassword, port);
            stmt = connection.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
        } catch (Exception e) {
            e.printStackTrace();
        }
        loadAccountList();
        pack();
        txtUsername.setText(account);
    }

